void main() {
  var marks = 20;
  switch (marks) {
    case 80:
      print('A+');
      break;
    case 70:
      print('A');
      break;
    case 60:
      print('A-');
      break;
    case 50:
      print('C');
      break;
    case 40:
      print("d");
      break;
    default:
      print('Result is not found');
  }
}
